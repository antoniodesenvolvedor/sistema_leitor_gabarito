    private void carregarComboBox(JComboBox comboBox){   
        if(listaTurmas != null){          
            int tamanho = listaTurmas.size();   
            String nomesTurmas[] = new String[tamanho + 1];
            nomesTurmas[0] = "";
            
            for(int i = 0; i < tamanho; i++){
                nomesTurmas[i+1] = listaTurmas.get(i).getDescricao();               
            }                   
            comboBox.setModel( new DefaultComboBoxModel( nomesTurmas ) ); 
        }
                
    }
    
     
    private void carregarTable(JTable jTable){

     

        if(listaAlunos != null){
            
            int tamanho = listaAlunos.size();
            String dadosAlunos[] = new String[4];
            DefaultTableModel model;         
            model = (DefaultTableModel) jTable.getModel();
            
            // limpando para evitar lixo ao atualizar a table
            int quantidadeLinhas = model.getRowCount();
            for(int i = 0; i < quantidadeLinhas; i++){
                model.removeRow(0);
            }
                                  
            for(int i = 0; i < tamanho; i++){
                dadosAlunos[0] = listaAlunos.get(i).getNome();
                dadosAlunos[1] = listaAlunos.get(i).getSobrenome();
                dadosAlunos[2] = String.valueOf(listaAlunos.get(i).getCodigo());
                dadosAlunos[3] = listaAlunos.get(i).getTurma().getDescricao();
                
                model.addRow(dadosAlunos);
            }
            
            jTable.setModel( model );                       
        }      
    }
      
    
    
