    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed

        Aluno aluno = new Aluno();
        aluno.setNome(jTextFieldNome.getText());
        aluno.setSobrenome(jTextFieldSobrenome.getText());
        try{
           aluno.setCodigo(Integer.parseInt(jTextFieldMatricula.getText())); 
        }
        catch(NumberFormatException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Valor inválido para matrícula");
            jTextFieldMatricula.requestFocus();
            return;
        }
        
        
        int indexCombo = jComboBoxTurma.getSelectedIndex();
        if(indexCombo == -1){
            JOptionPane.showMessageDialog(null, "Selecione uma turma para o aluno");
            return;
        }
        aluno.setTurma( listaTurmas.get( indexCombo - 1 ) );
        
        
        if(alterar){
           int indexTable = jTablePesquisa.getSelectedRow();
           if(indexTable == -1){
               return;
           }
           indexTable = tableSorter.convertRowIndexToModel(indexTable);
            
           aluno.setCodigo( listaAlunos.get( indexTable ).getCodigo() );
           boolean exito = aluno.alterar();
            if(exito){
              JOptionPane.showMessageDialog(null,"Aluno " + aluno.getNome() +" " + aluno.getSobrenome() + " Alterado com sucesso");
            }
            else{
                JOptionPane.showMessageDialog(null,"Não foi possível alterar os dados aluno,"
                        + " verifique se todos os campos foram preenchidos corretamente");
                return;
            } 
            
        }
        else{    
            boolean exito = aluno.salvar();
            if(exito){
              JOptionPane.showMessageDialog(null,"Aluno " + aluno.getNome() +" " + aluno.getSobrenome() + " Salvo com sucesso");  
            }
            else{
                JOptionPane.showMessageDialog(null,"Não foi possível salvar o aluno,"
                        + " verifique se todos os campos foram preenchidos");
                return;
            }
        }
        
        listaAlunos = Aluno.carregarLista();
        carregarTable(jTablePesquisa);
        setComponents(true,false,false,false,false,false,false);
                       
    }//GEN-LAST:event_jButtonSalvarActionPerformed

